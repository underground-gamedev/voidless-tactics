using System;
using System.Collections.Generic;

public static class RandExtend
{
    public static void Shuffle<T>(this Random rng, IList<T> list)
    {
        int n = list.Count;  
        while (n > 1) {  
            n--;  
            int k = rng.Next(n + 1);  
            T value = list[k];  
            list[k] = list[n];  
            list[n] = value;  
        }  
    }
}